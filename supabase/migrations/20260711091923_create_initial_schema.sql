-- ============================================================
-- SECTION: RLS policies
-- ============================================================

CREATE POLICY "Profiles are viewable by authenticated users"
ON public.profiles
FOR SELECT
USING (auth.role() = 'authenticated');

CREATE POLICY "Users can update their own profile"
ON public.profiles
FOR UPDATE
USING (id = auth.uid())
WITH CHECK (id = auth.uid());

CREATE POLICY "No direct profile inserts"
ON public.profiles
FOR INSERT
WITH CHECK (false);

CREATE POLICY "Categories are viewable by everyone"
ON public.categories
FOR SELECT
USING (true);

CREATE POLICY "Only admins can insert categories"
ON public.categories
FOR INSERT
WITH CHECK (public.is_admin());

CREATE POLICY "Only admins can update categories"
ON public.categories
FOR UPDATE
USING (public.is_admin())
WITH CHECK (public.is_admin());

CREATE POLICY "Only admins can delete categories"
ON public.categories
FOR DELETE
USING (public.is_admin());

CREATE POLICY "Recipes are viewable by published recipes and own drafts"
ON public.recipes
FOR SELECT
USING (
  is_published = true
  OR (auth.role() = 'authenticated' AND user_id = auth.uid())
  OR public.is_admin()
);

CREATE POLICY "Users can insert their own recipes"
ON public.recipes
FOR INSERT
WITH CHECK (user_id = auth.uid());

CREATE POLICY "Users can update their own recipes"
ON public.recipes
FOR UPDATE
USING (user_id = auth.uid())
WITH CHECK (user_id = auth.uid());

CREATE POLICY "Users can delete their own recipes"
ON public.recipes
FOR DELETE
USING (user_id = auth.uid());

CREATE POLICY "Admins can update every recipe"
ON public.recipes
FOR UPDATE
USING (public.is_admin())
WITH CHECK (public.is_admin());

CREATE POLICY "Admins can delete every recipe"
ON public.recipes
FOR DELETE
USING (public.is_admin());

CREATE POLICY "Users can view their own role"
ON public.user_roles
FOR SELECT
USING (user_id = auth.uid());

CREATE POLICY "Admins can view all roles"
ON public.user_roles
FOR SELECT
USING (public.is_admin());

CREATE POLICY "Only admins can update roles"
ON public.user_roles
FOR UPDATE
USING (public.is_admin())
WITH CHECK (public.is_admin());

CREATE POLICY "No direct role inserts"
ON public.user_roles
FOR INSERT
WITH CHECK (false);

CREATE POLICY "No direct role deletes"
ON public.user_roles
FOR DELETE
USING (false);

